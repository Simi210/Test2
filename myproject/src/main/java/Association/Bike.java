package Association;

public class Bike {
String bikeName;
int bikeRegNo;

public Bike(String bikeName,int  bikeRegNo) {
	this.bikeName=bikeName;
	this.bikeRegNo=bikeRegNo;
}

public String getBikeName() {
	return bikeName;
}

public int getBikeRegNo() {
	return bikeRegNo;
}



}
